#!/bin/bash

mkdir -p peptides
# traduzir
for f in 1.filtered/*.fasta; do
    base=$(basename "${f%.fasta}")

    transeq \
        -sequence "$f" \
        -outseq "peptides/${base}.pep.fasta" \
        -clean
done

mkdir -p aligned_protein
#align protein
for f in peptides/*.pep.fasta; do
    base=$(basename "$f" .pep.fasta)
    mafft "$f" > "aligned_protein/${base}.aln.fasta"
done

#resultado do mafft
#disttbfast (aa) Version 7.526
#alg=A, model=BLOSUM62, 1.53, -0.00, -0.00, noshift, amax=0.0
#0 thread(s)

#Strategy:
#FFT-NS-2 (Fast but rough)
#Progressive method (guide trees were built 2 times.)

#If unsure which option to use, try 'mafft --auto input > output'.
#For more information, see 'mafft --help', 'mafft --man' and the mafft page.

#The default gap scoring scheme has been changed in version 7.110 (2013 Oct).
#It tends to insert more gaps into gap-rich regions than previous versions.
#To disable this change, add the --leavegappyregion option.

#nthread = 0
#nthreadpair = 0
#nthreadtb = 0
#ppenalty_ex = 0
#stacksize: 8176 kb->17505 kb
#rescale = 1
#Gap Penalty = -1.53, +0.00, +0.00

mkdir -p tranalign
module load emboss/6.6.0


#codon alignment 
for aln in aligned_protein/*.aln.fasta; do
  base=$(basename "$aln" .aln.fasta)

  cds="1.filtered/${base}.fasta"

  out="tranalign/${base}.codon_aln.fasta"

  [[ -s "$aln" && -s "$cds" ]] || continue
    # Ensure protein sequence names match the CDS headers.
    temp_prot="$aln"

    # Check if any protein header is missing in the CDS file. If so,
    # try stripping a trailing numeric suffix like _1, _2, etc.
    need_strip=0
    while IFS= read -r h; do
        name=${h#>} 
        if ! grep -qF "$name" "$cds"; then
            # try stripped name
            stripped=$(echo "$name" | sed -E 's/_[0-9]+$//')
            if grep -qF "$stripped" "$cds"; then
                need_strip=1
            else
                need_strip=2
                break
            fi
        fi
    done < <(grep '^>' "$aln")

    if [ "$need_strip" -eq 2 ]; then
        echo "Skipping ${base}: protein headers do not match CDS headers" >&2
        continue
    fi

    if [ "$need_strip" -eq 1 ]; then
        tmpfile="tranalign/tmp_${base}.prot_aln.fasta"
        awk '/^>/{sub(/_[0-9]+$/,"",$0)} {print}' "$aln" > "$tmpfile"
        temp_prot="$tmpfile"
    fi

    tranalign \
        -asequence "$cds" \
        -bsequence "$temp_prot" \
        -outseq "$out"

    if [ "$temp_prot" != "$aln" ]; then
        rm -f "$temp_prot"
    fi
done

#!/bin/bash
#PBS -N tranalign
#PBS -l select=1:ncpus=4:mpiprocs=1:ompthreads=4
#PBS -l walltime=04:00:00
#PBS -j oe

module load trimal/1.5.1
mkdir -p trimmed_protein

# 6. trim
for codon in tranalign/*.codon_aln.fasta; do
    base=$(basename "$codon" .codon_aln.fasta)
out="trimmed_protein/${base}.codon_aln.trimmed.fasta"
    trimal -in "$codon" \
        -out "$out" \
        -automated1 || true
done

mkdir -p mimoseae_species_summary

for aln in trimmed_protein/*.codon_aln.trimmed.fasta; do
    base=$(basename "$aln" .codon_aln.trimmed.fasta)
    echo "$base" > mimoseae_species_summary/${base}.species.txt
    grep '^>' "$aln" | sed 's/^>//' | sort >> mimoseae_species_summary/${base}.species.txt
done

